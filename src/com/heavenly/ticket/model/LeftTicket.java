package com.heavenly.ticket.model;

public class LeftTicket {
	private String id;
	private String name;
	private String stationGetOn;
	private String timeGetOn;
	private String stationGetOff;
	private String timeGetOff;
	private String tripLast;
	public String getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public String getStationGetOn() {
		return stationGetOn;
	}
	public String getTimeGetOn() {
		return timeGetOn;
	}
	public String getStationGetOff() {
		return stationGetOff;
	}
	public String getTimeGetOff() {
		return timeGetOff;
	}
	public String getTripLast() {
		return tripLast;
	}

	
}